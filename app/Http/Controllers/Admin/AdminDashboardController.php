     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function status($id)
    {
        $contacts = Contact::find($id);
        if($contacts->status == 0){
            $contacts->status = 1;
            $contacts->save();
            return back();
        }else{
            $contacts->status = 0;
            $contacts->save();
            return back();
        }
    }

